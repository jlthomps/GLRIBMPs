#'RMevents
#'
#'function to compute rainfall event variables based on time series of rain data with only one rain gage or 
#'one mean radar rain column
#'
#'@param df data frame containing ADAPS precip data
#'@param ieHr number value of desired interval between storm events
#'@param rainthresh value of desired rain depth threshold minimum for event, in units of rain column
#'@param rain string name of column in df containing rainfall unit values
#'@param time string name of column in df containing datetime in POSIXct
#'@return storms_list list of storms that meet the ieHr and rainthresh requirements and all storms
RMevents <- function(df,ieHr=6,rainthresh=5.1,rain="rain",time="pdate"){
  
  ieSec <- ieHr * 3600 # compute interevent period in seconds to use with POSIX
  
  # Initiate variables
  StartRow <- 1
  EndRow <- 1
  StartDryRow <- 1
  dry <- TRUE
  stormnum <- 0
  continue.dry <- TRUE
  sumrain <- 0
  
  # Loop through rain data and define event periods
  for (i in 2:nrow(df)) {
    
    # During dry period, look for start of event
    if(dry) {
      
      # Event initiation
      if(df[i,rain]>0) {  
        dry=FALSE
        StartRow <- i-1
      }
    }
    # Define event period
    if(!dry) {
      
      # Search for end of event period
      if(df[i,rain]==0) {
        if(!continue.dry){
          continue.dry <- TRUE
          dryduration <- difftime(df[i,time],
                                  df[StartDryRow,time],
                                  units="secs")
        }
        
        # Continue checking for end of event (dry duration >= interevent period)
        if(continue.dry){                   
          dryduration <- difftime(df[i,time],df[StartDryRow,time],units="secs")
          if(dryduration >= ieSec) {
            EndRow <- StartDryRow
            stormnum <- stormnum + 1
            
            # After event period ends, save start and end dates/times and rain depth
            current.storm <- data.frame(stormnum=stormnum,
                                        StartDate=df[StartRow,time],
                                        EndDate=df[EndRow,time],
                                        rain=sumrain)
            dry <- TRUE
            if(stormnum>1) storms <- rbind(storms,current.storm)
            else storms <- current.storm        
            sumrain <- 0
            
          }
        }
      }
      # add current rain to event depth
      if (df[i,rain]!=0) {
        sumrain <- sumrain + df[i,rain]
        EndRow <- i
        StartDryRow <- EndRow
        continue.dry <- FALSE
      }
    }
  }
  
  # Subset based on defined event rain depth threshold        
  storms2 <- subset(storms,rain>=rainthresh,row.names=FALSE)
  storms_list <- list(storms2,storms)
  
  return(storms_list)
}